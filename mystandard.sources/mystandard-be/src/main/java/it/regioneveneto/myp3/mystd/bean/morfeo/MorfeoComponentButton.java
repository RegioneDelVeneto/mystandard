package it.regioneveneto.myp3.mystd.bean.morfeo;

public class MorfeoComponentButton {

    private String label;
    private String icon;
    private String action;
    private String color;
    private String style;

    //Getter and setter

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    //Constructor

    public MorfeoComponentButton() {
    }

    public MorfeoComponentButton(String label, String icon, String action, String color, String style) {
        this.label = label;
        this.icon = icon;
        this.action = action;
        this.color = color;
        this.style = style;
    }
}
